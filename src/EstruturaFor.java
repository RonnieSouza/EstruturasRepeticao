public class EstruturaFor {
    public static void main(String[] args) {
        //contar ate um numero
        for (int cont = 1; cont <= 20; cont ++) {
            System.out.println("Contando: " + cont);
        }
        System.out.println("--------------------");
        System.out.println("Terminou a contagem!");
        System.out.println("--------------------");
    }
}
